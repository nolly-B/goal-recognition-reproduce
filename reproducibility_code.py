@dataclass
class Observation:
    action_name: str
    parameters: List[str]

    def to_pddl(self) -> str:
        if self.parameters:
            return f"({self.action_name} {' '.join(self.parameters)})"
        return f"({self.action_name})"

    def get_predicate_name(self) -> str:
        return f"obs_{self.action_name}_{'_'.join(self.parameters)}"


@dataclass
class PlanningResult:
    success: bool
    plan_cost: float
    plan_length: int
    plan_actions: List[str]
    runtime: float


class PDDLCompiler:

    def __init__(self, domain_path: str, problem_path: str):

        self.domain_path = Path(domain_path)
        self.problem_path = Path(problem_path)


        self.domain_content = self.domain_path.read_text()
        self.problem_content = self.problem_path.read_text()

        # Extract domain name and problem name
        self.domain_name = self._extract_domain_name()
        self.problem_name = self._extract_problem_name()

        # Extract original goal
        self.original_goal = self._extract_goal()

        # Extract objects and their types
        self.objects = self._extract_objects()

        # Extract action schemas from domain
        self.actions = self._extract_actions()

    def _extract_domain_name(self) -> str:

        match = re.search(r'\(domain\s+(\S+)\)', self.domain_content)
        if match:
            return match.group(1)
        return "unknown"

    def _extract_problem_name(self) -> str:

        match = re.search(r'\(problem\s+(\S+)\)', self.problem_content)
        if match:
            return match.group(1)
        return "unknown"

    def _extract_goal(self) -> str:

        # Find the goal section
        goal_pattern = r'\(:goal\s+(.+?)\)(?:\s|$|\(:|\n)'
        match = re.search(goal_pattern, self.problem_content, re.DOTALL)
        if match:
            goal = match.group(1).strip()

            return goal
        return ""

    def _extract_objects(self) -> Dict[str, List[str]]:

        objects = {}
        obj_pattern = r'\(:objects\s+(.+?)\)(?:\s|$|\(:|\n)'
        match = re.search(obj_pattern, self.problem_content, re.DOTALL)
        if match:
            obj_decl = match.group(1)
            # Parse tokens, handling types (e.g., "a b c - type")
            tokens = obj_decl.strip().split()
            current_type = "object"
            current_objs = []

            for token in tokens:
                if token == "-":
                    continue
                elif token in ["object", "thing"] or (current_type == "object" and token not in current_objs):
                    if current_objs:
                        objects[current_type] = current_objs
                        current_objs = []
                    current_type = token
                else:
                    current_objs.append(token)

            if current_objs:
                objects[current_type] = current_objs

        return objects

    def _extract_actions(self) -> List[Dict]:
        actions = []
        action_pattern = r'\(:action\s+(\S+)\s*:parameters\s*\((.+?)\)\s*:precondition\s*(.+?)\s*:effect\s*(.+?)\)'

        for match in re.finditer(action_pattern, self.domain_content, re.DOTALL):
            action = {
                'name': match.group(1),
                'parameters': match.group(2).strip(),
                'precondition': match.group(3).strip(),
                'effect': match.group(4).strip()
            }
            actions.append(action)

        return actions

    def compile_compliant(
        self,
        observations: List[Observation],
        output_dir: Optional[Path] = None
    ) -> Tuple[str, str]:

        if output_dir is None:
            output_dir = Path(tempfile.mkdtemp())
        else:
            output_dir = Path(output_dir)
            output_dir.mkdir(parents=True, exist_ok=True)

        # Create augmented domain
        augmented_domain = self._create_compliant_domain(observations)
        domain_out = output_dir / f"{self.domain_name}_compliant.pddl"
        domain_out.write_text(augmented_domain)

        # Create augmented problem
        augmented_problem = self._create_compliant_problem(observations)
        problem_out = output_dir / f"{self.problem_name}_compliant.pddl"
        problem_out.write_text(augmented_problem)

        return str(domain_out), str(problem_out)

    def _create_compliant_domain(self, observations: List[Observation]) -> str:

        # Add observation predicates
        obs_predicates = []
        for obs in observations:
            pred_name = obs.get_predicate_name()
            # Create predicate: (obs_<action> ?p1 ?p2 ...)
            params_str = " ".join([f"?p{i}" for i in range(len(obs.parameters))])
            obs_predicates.append(f"({pred_name} {params_str})")

        # Build new domain by modifying actions to add observation effects
        modified_actions = []

        for action in self.actions:
            # For each action that matches an observation, add the observation effect
            original_effect = action['effect']
            additional_effects = []

            for obs in observations:
                if self._action_matches_observation(action, obs):

                    pred_name = obs.get_predicate_name()
                    params_str = " ".join(obs.parameters)
                    additional_effects.append(f"({pred_name} {params_str})")

            if additional_effects:
                # Combine original effect with new effects
                if original_effect.startswith('(and'):
                    new_effect = original_effect[:-1] + " " + " ".join(additional_effects) + ")"
                else:

                    all_effects = [original_effect] + additional_effects
                    new_effect = f"(and {' '.join(all_effects)})"
            else:
                new_effect = original_effect

            # Create modified action
            modified_action = f"""
(:action {action['name']}
  :parameters ({action['parameters']})
  :precondition {action['precondition']}
  :effect {new_effect})
"""
            modified_actions.append(modified_action)

        # Build the complete domain
        obs_predicates_str = "\n    ".join(obs_predicates) if obs_predicates else ""

        # Find the existing predicates section
        pred_match = re.search(r'\(:predicates\s+(.+?)\)', self.domain_content, re.DOTALL)
        if pred_match:
            existing_preds = pred_match.group(1).strip()
            # Insert new predicates
            new_domain = self.domain_content.replace(
                f'(:predicates {existing_preds})',
                f'(:predicates {existing_preds} {" ".join(obs_predicates)})' if obs_predicates else f'(:predicates {existing_preds})'
            )
        else:
            # If no predicates section, add one
            pred_section = f'(:predicates\n    {" ".join(obs_predicates)})' if obs_predicates else '(:predicates)'
            # Insert after domain name
            domain_header = f'(domain {self.domain_name})'
            new_domain = self.domain_content.replace(
                domain_header,
                f'{domain_header}\n{pred_section}'
            )

        # Replace actions
        for i, action in enumerate(self.actions):
            original_action_str = self._extract_action_string(action['name'])
            if original_action_str:
                new_domain = new_domain.replace(original_action_str, modified_actions[i])

        return new_domain

    def _create_compliant_problem(self, observations: List[Observation]) -> str:


        obs_goals = []
        for obs in observations:
            pred_name = obs.get_predicate_name()
            params_str = " ".join(obs.parameters)
            obs_goals.append(f"({pred_name} {params_str})")

        # Combine original goal with observation goals
        if obs_goals:
            if self.original_goal:
                if self.original_goal.startswith('(and'):
                    # Insert into existing and
                    combined_goal = self.original_goal[:-1] + " " + " ".join(obs_goals) + ")"
                else:
                    combined_goal = f"(and {self.original_goal} {' '.join(obs_goals)})"
            else:
                combined_goal = f"(and {' '.join(obs_goals)})"
        else:
            combined_goal = self.original_goal

        # Build the new problem file
        new_problem = self.problem_content

        # Replace the goal
        goal_pattern = r'\(:goal\s+.+?\)'
        new_problem = re.sub(goal_pattern, f'(:goal {combined_goal})', new_problem, flags=re.DOTALL)

        return new_problem

    def compile_non_compliant(
        self,
        observations: List[Observation],
        output_dir: Optional[Path] = None
    ) -> Tuple[str, str]:

        if output_dir is None:
            output_dir = Path(tempfile.mkdtemp())
        else:
            output_dir = Path(output_dir)
            output_dir.mkdir(parents=True, exist_ok=True)


        augmented_domain = self._create_non_compliant_domain(observations)
        domain_out = output_dir / f"{self.domain_name}_noncompliant.pddl"
        domain_out.write_text(augmented_domain)

        augmented_problem = self._create_non_compliant_problem(observations)
        problem_out = output_dir / f"{self.problem_name}_noncompliant.pddl"
        problem_out.write_text(augmented_problem)

        return str(domain_out), str(problem_out)

    def _create_non_compliant_domain(self, observations: List[Observation]) -> str:


        forbidden_pred = "(forbidden)"

        # Add forbidden predicate to domain
        pred_match = re.search(r'\(:predicates\s+(.+?)\)', self.domain_content, re.DOTALL)
        if pred_match:
            existing_preds = pred_match.group(1).strip()
            new_domain = self.domain_content.replace(
                f'(:predicates {existing_preds})',
                f'(:predicates {existing_preds} {forbidden_pred})'
            )
        else:
            domain_header = f'(domain {self.domain_name})'
            new_domain = self.domain_content.replace(
                domain_header,
                f'{domain_header}\n(:predicates {forbidden_pred})'
            )


        return self.domain_content

    def _create_non_compliant_problem(self, observations: List[Observation]) -> str:

        return self.problem_content

    def _action_matches_observation(self, action: Dict, obs: Observation) -> bool:

        if action['name'] != obs.action_name:
            return False


        param_names = re.findall(r'\?(\w+)', action['parameters'])

        if len(param_names) != len(obs.parameters):
            return False

        return True

    def _extract_action_string(self, action_name: str) -> Optional[str]:

        pattern = rf'\(:action\s+{re.escape(action_name)}.+?\)(?=\s*\(:action|\s*\(:)\s*'
        match = re.search(pattern, self.domain_content, re.DOTALL)
        if match:
            return match.group(0)
        return None


class GoalRecognizer:


    def __init__(
        self,
        domain_path: str,
        problem_template: str,
        planner_command: List[str],
        beta: float = 0.5
    ):

        self.domain_path = Path(domain_path)
        self.problem_template = Path(problem_template)
        self.planner_command = planner_command
        self.beta = beta

    def compute_cost_difference(
        self,
        goal: str,
        observations: List[Observation],
        compiler: PDDLCompiler
    ) -> Tuple[float, float, float]:




        # Create problem with this goal
        problem = self._create_goal_problem(goal)

        # Compute compliant plan cost
        with tempfile.TemporaryDirectory() as tmpdir:
            tmp_path = Path(tmpdir)
            problem_file = tmp_path / "problem.pddl"
            problem_file.write_text(problem)

            # Create compiler for this goal-specific problem
            goal_compiler = PDDLCompiler(str(self.domain_path), str(problem_file))

            # Compile compliant version
            dom_compliant, prob_compliant = goal_compiler.compile_compliant(observations, tmp_path / "compliant")

            # Run planner for compliant
            compliant_result = self._run_planner(dom_compliant, prob_compliant)


            noncompliant_result = self._run_planner(str(self.domain_path), str(problem_file))

        compliant_cost = compliant_result.plan_cost if compliant_result.success else float('inf')
        noncompliant_cost = noncompliant_result.plan_cost if noncompliant_result.success else float('inf')

        delta = compliant_cost - noncompliant_cost

        return compliant_cost, noncompliant_cost, delta

    def _create_goal_problem(self, goal: str) -> str:

        problem_content = self.problem_template.read_text()

        # Replace goal placeholder
        goal_pattern = r'\(:goal\s+.+?\)'
        problem_content = re.sub(goal_pattern, f'(:goal {goal})', problem_content, flags=re.DOTALL)

        return problem_content

    def _run_planner(self, domain_path: str, problem_path: str) -> PlanningResult:

        import time


        start_time = time.time()



        plan_file = Path(tempfile.mktemp(suffix='.plan'))

        # Build command - customize for your planner
        cmd = self.planner_command + [domain_path, problem_path, '--plan-file', str(plan_file)]

        try:
            result = subprocess.run(
                cmd,
                capture_output=True,
                text=True,
                timeout=300  # 5 minute timeout
            )

            runtime = time.time() - start_time

            if result.returncode == 0 and plan_file.exists():
                plan_cost, plan_actions = self._parse_plan(plan_file)
                return PlanningResult(
                    success=True,
                    plan_cost=plan_cost,
                    plan_length=len(plan_actions),
                    plan_actions=plan_actions,
                    runtime=runtime
                )
            else:
                return PlanningResult(
                    success=False,
                    plan_cost=float('inf'),
                    plan_length=0,
                    plan_actions=[],
                    runtime=runtime
                )
        except subprocess.TimeoutExpired:
            return PlanningResult(
                success=False,
                plan_cost=float('inf'),
                plan_length=0,
                plan_actions=[],
                runtime=300
            )
        finally:
            if plan_file.exists():
                plan_file.unlink()

    def _parse_plan(self, plan_file: Path) -> Tuple[float, List[str]]:
        """Parse the plan file to extract actions and cost"""
        content = plan_file.read_text()

        # Look for cost in various formats
        cost = 1.0  # default cost per action

        # Some planners output "; cost = X" or "Plan cost: X"
        cost_match = re.search(r'cost\s*[=:]\s*(\d+(?:\.\d+)?)', content, re.IGNORECASE)
        if cost_match:
            total_cost = float(cost_match.group(1))
        else:
            # Count actions as cost
            actions = []
            for line in content.split('\n'):
                line = line.strip()
                if line and not line.startswith(';') and line.startswith('('):
                    actions.append(line)
            total_cost = float(len(actions))
            return total_cost, actions

        # Extract individual actions
        actions = re.findall(r'\([^)]+\)', content)
        actions = [a for a in actions if not a.startswith(';')]

        return total_cost, actions

    def compute_posterior(
        self,
        goals: List[str],
        observations: List[Observation],
        compiler: PDDLCompiler,
        prior: Optional[List[float]] = None
    ) -> Dict[str, float]:

        n_goals = len(goals)

        if prior is None:
            prior = [1.0 / n_goals] * n_goals

        # Compute likelihoods
        likelihoods = []
        deltas = []

        for goal in goals:
            _, _, delta = self.compute_cost_difference(goal, observations, compiler)
            deltas.append(delta)

            # Boltzmann likelihood
            likelihood = np.exp(self.beta * delta)
            likelihoods.append(likelihood)

        # Normalize to get P(O|G)
        likelihoods = np.array(likelihoods)
        likelihoods = likelihoods / likelihoods.sum()

        # Apply prior
        posterior = likelihoods * np.array(prior)
        posterior = posterior / posterior.sum()

        return {f"Goal_{i}": p for i, p in enumerate(posterior)}


class ObservationGenerator:


    def __init__(self, domain_path: str, problem_template: str, planner_command: List[str]):
        self.domain_path = domain_path
        self.problem_template = problem_template
        self.planner_command = planner_command

    def generate_plan(self, goal: str) -> Tuple[List[str], float]:

        # Create problem with this goal
        problem_content = Path(self.problem_template).read_text()
        goal_pattern = r'\(:goal\s+.+?\)'
        problem_content = re.sub(goal_pattern, f'(:goal {goal})', problem_content, flags=re.DOTALL)

        with tempfile.TemporaryDirectory() as tmpdir:
            problem_file = Path(tmpdir) / "problem.pddl"
            problem_file.write_text(problem_content)

            # Run planner
            cmd = self.planner_command + [self.domain_path, str(problem_file)]
            result = subprocess.run(cmd, capture_output=True, text=True)

            if result.returncode == 0:
                # Parse plan from output
                actions = []
                for line in result.stdout.split('\n'):
                    line = line.strip()
                    if line and line.startswith('('):
                        actions.append(line)
                return actions, len(actions)

        return [], float('inf')

    def generate_observation_prefix(
        self,
        goal: str,
        observation_ratio: float
    ) -> Tuple[List[Observation], str]:

        plan, plan_length = self.generate_plan(goal)

        if not plan:
            return [], goal

        n_obs = max(1, int(len(plan) * observation_ratio))
        observed_actions = plan[:n_obs]

        observations = []
        for action_str in observed_actions:
            obs = self._parse_action(action_str)
            observations.append(obs)

        return observations, goal

    def _parse_action(self, action_str: str) -> Observation:
        """Parse a PDDL action string into an Observation object"""
        # Remove parentheses
        action_str = action_str.strip()
        if action_str.startswith('(') and action_str.endswith(')'):
            action_str = action_str[1:-1]

        parts = action_str.split()
        action_name = parts[0]
        parameters = parts[1:] if len(parts) > 1 else []

        return Observation(action_name=action_name, parameters=parameters)


