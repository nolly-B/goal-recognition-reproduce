def compute_statistics(results_file: Path) -> Dict:
    """Compute means, stds, confidence intervals from results"""
    with open(results_file, 'r') as f:
        data = json.load(f)

    stats = {}
    for obs_ratio, records in data.items():
        accuracies = [r['accuracy'] for r in records]
        posteriors = [r['true_posterior'] for r in records]
        ranks = [r['rank'] for r in records]

        n = len(accuracies)
        stats[obs_ratio] = {
            'accuracy_mean': np.mean(accuracies),
            'accuracy_std': np.std(accuracies),
            'accuracy_ci_95': 1.96 * np.std(accuracies) / np.sqrt(n),
            'posterior_mean': np.mean(posteriors),
            'posterior_std': np.std(posteriors),
            'rank_mean': np.mean(ranks),
            'n': n
        }

    return stats


