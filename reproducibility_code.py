class GoalRecognizer:
    """Main recognizer implementing Ramirez & Geffner 2010"""

    def __init__(self, planner: FastDownwardPlanner, beta: float = 1.0):
        self.planner = planner
        self.beta = beta

    def recognize(self, domain_file: Path,
                  problems: Dict[str, Path],  # goal_name -> problem_file
                  observations: List[str],
                  temp_dir: Path) -> Dict[str, float]:
        """
        Recognize goal from observations

        Args:
            domain_file: Path to domain PDDL
            problems: Dict mapping goal names to problem PDDL files
            observations: List of observed action names
            temp_dir: Directory for temporary compiled files

        Returns:
            Dictionary mapping goal names to posterior probabilities
        """
        compiler = PDDLCompiler(str(domain_file), "")
        costs = {}

        for goal_name, problem_file in problems.items():
            # Compliant problem
            comp_domain, comp_problem = compiler.compile_compliant(
                observations, temp_dir
            )
            cost_comp = self.planner.get_plan_cost(comp_domain, comp_problem)

            # Non-compliant problem
            non_domain, non_problem = compiler.compile_noncompliant(
                observations, temp_dir
            )
            cost_non = self.planner.get_plan_cost(non_domain, non_problem)

            # Delta and likelihood
            delta = cost_non - cost_comp if cost_non != float('inf') else -1e6
            costs[goal_name] = delta

        # Boltzmann likelihoods
        max_delta = max(costs.values())
        likelihoods = {}
        for goal, delta in costs.items():
            # Numerical stability: subtract max
            likelihoods[goal] = np.exp(self.beta * (delta - max_delta))

        # Normalize to posterior
        total = sum(likelihoods.values())
        posteriors = {g: l/total for g, l in likelihoods.items()}

        return posteriors


