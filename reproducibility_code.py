class FastDownwardPlanner:
    """Wrapper for calling Fast Downward and extracting plan costs"""

    def __init__(self, fd_path: str, timeout: int = 300):
        self.fd_path = Path(fd_path)
        self.timeout = timeout

    def get_plan_cost(self, domain_file: Path, problem_file: Path) -> Optional[float]:
        """Run Fast Downward and return minimal plan cost"""
        # Command for Fast Downward with A* and LM-Cut heuristic
        cmd = [
            str(self.fd_path / "fast-downward.py"),
            "--alias", "seq-opt-lmcut",  # Optimal planning with LM-Cut
            "--overall-time-limit", str(self.timeout),
            str(domain_file),
            str(problem_file)
        ]

        try:
            result = subprocess.run(
                cmd,
                capture_output=True,
                text=True,
                timeout=self.timeout + 10
            )

            # Parse cost from output
            cost = self._parse_cost(result.stdout)
            return cost

        except subprocess.TimeoutExpired:
            print(f"Timeout on {domain_file}")
            return float('inf')
        except Exception as e:
            print(f"Error: {e}")
            return None

    def _parse_cost(self, output: str) -> Optional[float]:
        """Extract plan cost from Fast Downward output"""
        # Look for "Plan length: X" or "Cost: Y"
        match = re.search(r"Plan cost: (\d+)", output)
        if match:
            return float(match.group(1))

        match = re.search(r"Plan length: (\d+)", output)
        if match:
            return float(match.group(1))

        return float('inf')


